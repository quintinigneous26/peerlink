class ISPDetector:
    """运营商识别器"""
    
    def __init__(self, stun_client: STUNClient):
        self.stun_client = stun_client
    
    async def detect(self) -> ISP:
        """
        检测当前运营商
        
        步骤：
        1. 通过 STUN 获取公网 IP
        2. 根据 IP 段匹配运营商
        """
        logger.info("开始运营商检测...")
        
        # 尝试多个 STUN 服务器
        for server in self.stun_client.servers:
            parts = server.split(":")
            host = parts[0]
            port = int(parts[1]) if len(parts) > 1 else 3478
            
            response = await self.stun_client.binding_request(host, port)
            
            if response.success and response.mapped_ip:
                isp = self._identify_by_ip(response.mapped_ip)
                logger.info(f"运营商检测完成: {isp.value}, 公网IP: {response.mapped_ip}")
                return isp
        
        logger.warning("运营商检测失败，使用默认值")
        return ISP.UNKNOWN
    
    def _identify_by_ip(self, ip: str) -> ISP:
        """根据 IP 地址识别运营商"""
        
        # 检查中国大陆运营商
        for isp, patterns in ISP_IP_RANGES.items():
            for pattern in patterns:
                if re.match(pattern, ip):
                    return isp
        
        # 检查香港运营商
        for isp, patterns in HK_ISP_RANGES.items():
            for pattern in patterns:
                if re.match(pattern, ip):
                    return isp
        
        # 检查新加坡运营商
        for isp, patterns in SG_ISP_RANGES.items():
            for pattern in patterns:
                if re.match(pattern, ip):
                    return isp
        
        # 根据私有 IP 判断
        if ip.startswith("10.") or ip.startswith("192.168.") or ip.startswith("172."):
            logger.debug("检测到私有 IP，可能在多层 NAT 后")
            return ISP.UNKNOWN
        
        return ISP.UNKNOWN
    
    def get_region(self, isp: ISP) -> Region:
        """根据运营商获取区域"""
        from ..config.isp_profiles import get_isp_profile
        profile = get_isp_profile(isp)
        return profile.region
